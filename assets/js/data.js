resume.DisplayHonors = function(){
    resume.honors.forEach( function(item){
        var HTMLhonor = HTMLhonorTemplate;
        HTMLhonor = HTMLhonor.replace("%name%", item.name);
        HTMLhonor = HTMLhonor.replace("%year%", item.year);
        if(item.info != null && item.info !== ""){
            HTMLhonor = HTMLhonor.replace("%info%", item.info);
        }
        else{
            HTMLhonor = HTMLhonor.replace("%info%", "");            
        }

        if(item.tags != null && item.tags !== ""){
            var HTMLtags = "";
            for (var tag of item.tags){
                HTMLtags += (" " + tag);
                
            }
            HTMLhonor = HTMLhonor.replace("%tags%", item.tags);
        }
        $("#honors").append(HTMLhonor);
    });

};


resume.DisplaySkills = function(){
    var HTMLscaleTemplate  = '<div class="row justify-content-center mt-2 mb-2"> <div class="col-12 col-md-2 text-center text-md-right mt-8em">%name%:</div> <div class="col-12 col-md-8 text-center text-md-left"> %skills% </div> </div>';
    var HTMLallSkills = '<div class="container work-card">';

    // scaled skills
    resume.scaledSkills.forEach(function(item) {
        var HTMLscaledSkills = HTMLscaledSkillsTemplate;
        HTMLscaledSkills = HTMLscaledSkills.replace("%category%", item.category);
        var HTMLscales = "";
        for  (var scale of item.scales){
            HTMLscale = HTMLscaleTemplate;
            HTMLscale = HTMLscale.replace("%name%", scale.name);
            var HTMLskills = '';
            for  (var skill of scale.skills){
                HTMLskills += ('<span class="skill">' + skill + ' </span>');            
            }
            HTMLscale = HTMLscale.replace("%skills%", HTMLskills);
            HTMLscales += HTMLscale;
        }
        HTMLscaledSkills = HTMLscaledSkills.replace("%scales%", HTMLscales);
        HTMLallSkills += HTMLscaledSkills;
    });

    // normal skills
    resume.skills.forEach(function(item) {
        var HTMLskills = HTMLskillsTemplate;
        HTMLskills = HTMLskills.replace("%category%", item.category);

        var HTMLlittleSkills = '';
        for  (var skill of item.skills){
            HTMLlittleSkills += ('<span class="skill">' + skill + ' </span>');            
        }
        HTMLskills = HTMLskills.replace("%skills%", HTMLlittleSkills);
        
        HTMLallSkills += HTMLskills;
    });    
    $("#skills").append(HTMLallSkills + "</div>");
};



